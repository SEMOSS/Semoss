package prerna.algorithm.learning.r;

public class RRoutineException extends Exception {

	private static final long serialVersionUID = 1L;

	public RRoutineException() {
		super();
	}
	
	public RRoutineException(String message) {
		super(message);
	}
	
	public RRoutineException(String message, Throwable throwable) {
		super(message, throwable);
	}
	
}
